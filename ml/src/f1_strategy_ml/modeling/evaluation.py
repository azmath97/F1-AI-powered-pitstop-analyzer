import json
from pathlib import Path
from typing import Any

import pandas as pd


class EvaluationReportWriter:
    def __init__(self, report_dir: Path) -> None:
        self.report_dir = report_dir
        self.report_dir.mkdir(parents=True, exist_ok=True)

    def write_json(self, model_name: str, payload: dict[str, Any]) -> Path:
        path = self.report_dir / f"{model_name}_evaluation.json"
        path.write_text(json.dumps(payload, indent=2, default=str), encoding="utf-8")
        return path

    def write_dashboard(
        self,
        model_name: str,
        metrics: dict[str, Any],
        feature_importance: dict[str, float],
        shap_importance: dict[str, float],
    ) -> Path:
        top_features = sorted(feature_importance.items(), key=lambda item: item[1], reverse=True)[
            :20
        ]
        top_shap = sorted(shap_importance.items(), key=lambda item: item[1], reverse=True)[:20]
        html = f"""
<!doctype html>
<html lang="en">
<head>
  <meta charset="utf-8" />
  <title>{model_name} Evaluation</title>
  <style>
    body {{ font-family: Inter, Arial, sans-serif; margin: 32px; color: #111827; }}
    h1 {{ margin-bottom: 4px; }}
    section {{ margin-top: 28px; }}
    table {{ border-collapse: collapse; width: 100%; max-width: 900px; }}
    th, td {{ border: 1px solid #d1d5db; padding: 8px 10px; text-align: left; }}
    th {{ background: #f3f4f6; }}
  </style>
</head>
<body>
  <h1>{model_name}</h1>
  <p>Offline evaluation dashboard generated by the training pipeline.</p>
  <section>
    <h2>Metrics</h2>
    {pd.DataFrame([metrics]).to_html(index=False)}
  </section>
  <section>
    <h2>Feature Importance</h2>
    {pd.DataFrame(top_features, columns=["feature", "importance"]).to_html(index=False)}
  </section>
  <section>
    <h2>SHAP Importance</h2>
    {pd.DataFrame(top_shap, columns=["feature", "mean_abs_shap"]).to_html(index=False)}
  </section>
</body>
</html>
"""
        path = self.report_dir / f"{model_name}_dashboard.html"
        path.write_text(html, encoding="utf-8")
        return path
